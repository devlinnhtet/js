// Boolean
console.log(true);
let ThisIsBoolean = true;
console.log(typeof ThisIsBoolean);

// Number
console.log(23);
console.log(23.44);
let ThisIsNumber = 23;
console.log(typeof ThisIsNumber);

// String
console.log('hello');
console.log('hello, world');
let ThisIsString = 'hello, world';
console.log(typeof ThisIsString);

// Null
console.log(null);
let ThisIsNull = null;
console.log(typeof ThisIsNull); // object

// Undefined
console.log(undefined);
let ThisIsUndefined;
console.log(typeof ThisIsUndefined);